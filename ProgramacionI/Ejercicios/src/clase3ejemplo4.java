//Ejemplo de operadores aritméticos combinados.

public class clase3ejemplo4 {

		public static void main(String[] args) {

			int i,j,h;
			i = 7;
			i *=3;
			j = 10;
			j -=2;
			h = 25;
			h%=3;
			
			System.out.println(" Suma combinada: i += 3 "+ " // i vale " + i);
			System.out.println(" Multiplicación combinada: i *= 3 "+ " // i vale " + i);
			System.out.println(" Resta combinada: j -= 2 "+ " // j vale " + j);
			System.out.println(" Resto combinada: h %= 3 "+ " // h vale " + h);
			
	    }
}
